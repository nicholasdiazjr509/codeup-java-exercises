public class StringExercise {
    public static void main(String[] args) {

        String message = "We don't need no education\n"
       + "We don't need no thought control\n";
        System.out.println(message);

        String msg1 = "Check \"this\" out!, \"s inside of \"s! \n";
        System.out.println(msg1);

        String msg2 = "In windows, the main drive is usually C:\\ \n";
        System.out.println(msg2);

        String msg3 = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(msg3);
    }
}
